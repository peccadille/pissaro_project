<p>Afin de permettre au lecteur de mieux se plonger dans l’intimité de l’artiste, nous reproduirons dans la V2 l’intégralité des lettres rédigées durant les séjours rouennais de Pissarro. </p>

<p>Les précieux commentaires critiques de l’historienne de l’art Janine Bailly-Herzberg étant protégés par le droit d’auteur, nous ne pouvons que renvoyer le lecteur vers les volumes qu’elle a édités. </p>

