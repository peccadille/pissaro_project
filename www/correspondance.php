<p>Tout au long de sa vie, Pissarro a entretenu une abondante correspondance avec ses proches : épouse et enfants, marchands, critiques, amis artistes… Ces lettres (plus de 1700), sont une source essentielle pour retracer la vie du peintre : elles nous renseignent sur son quotidien, ses préoccupations, ses voyages et bien évidemment, sur la réalisation de ses tableaux.</p>

