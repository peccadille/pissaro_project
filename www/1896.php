              <div class="text_long">
              <p>En 1896, Pissarro décide de retourner à Rouen. Il est dans une période financièrement difficile et espère beaucoup de ce séjour : Durand-Ruel n’avait-il pas acheté la moitié de ses toiles de 1883 ? Une prochaine exposition de ses œuvres est prévue pour avril 1896 et Pissarro ambitionne produire une trentaine de peintures. Il se rend en avril 1895 dans la capitale normande pour trouver un hôtel : il ne lui est plus possible de loger à l’hôtel du Dauphin et d’Espagne, que Mürer a vendu. De toute façon, les relations entre les deux hommes se sont refroidies.</p>

              <p>En 1896, le choix des chambres d’hôtel est beaucoup plus crucial qu’en 1883. Le peintre, âgé de 66 ans souffre d’une infection chronique aux yeux, ce qui lui interdit de travailler en plein air : au moindre courant d’air, il risque de voir son mal empirer. Il peindra donc depuis sa fenêtre, qui doit nécessairement lui offrir une jolie vue. </p>

              <p>« <i>Je vais chercher un hôtel sur les quais d’où je pourrai faire des tableaux très travaillés sans trop de fatigue, pour porter des toiles de trente. C’est d’une grande difficulté, à présent je suis vieux et sans argent. Je ne sais pas quand je viendrai ici, peut-être l’été, peut-être l’automne.</i> «  (lettre n° 1130)</p>

              <p>Son choix se porte sur l’hôtel de Paris, situé sur le quai. Le confort ne lui donne pas satisfaction : il fait très froid dans les chambres et, faute d’argent, Pissarro peine à se chauffer. Mais la vue est belle : de la fenêtre le regard embrasse la Seine jusqu’au pont Corneille et l’île Lacroix. À gauche, il y a le pont Boiledieu, que Pissarro va beaucoup représenter durant son séjour. Entre le 20 janvier et le 30 mars, il peint quinze tableaux du port : Durand-Ruel lui en achète dix.</p>

              <p>Le 8 septembre, Pissarro est de retour à Rouen. Il y restera jusqu’au 11 novembre. Cette fois-ci, c’est à l’hôtel d’Angleterre qu’il est installé. La belle vue sur le port lui faisait envie depuis son précédent séjour, mais jusque là ses finances de lui avaient pas permis de séjourner dans cet hôtel un peu plus cossu. Il retient une chambre que Monet avant lui avait occupée en 1892. Il y peint quinze vues du port, dont onze seront achetées par Durand-Ruel. </p>

              <p>Il poursuit la série commencée au mois de janvier, se régalant du paysage : les trois ponts en enfilade, la gare d’Orléans, le quai de la Bourse ; DEVELOPPER ANALYSE TABLEAUX. </p>

              <p>Les tableaux peints pendant les deux campagnes rouennaises de 1896 sont immédiatement présentés chez Durand-Ruel, qui consacre deux expositions au peintre : l’une à Paris (avril 1896) autour de ses œuvres récentes, l’autre à New York (avril 1897) intitulée Views of Rouen. Si la critique est positive, le marchand vend peu : deux toiles seulement trouvent preneurs ! </p>


