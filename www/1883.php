            <h2>Le premier séjour : 1883</h2>
              <div class="text_long">
                <p>À l’automne 1883, Pissarro effectue son premier séjour rouennais. Pourquoi Rouen ? Probablement le peintre Claude Monet, dont le frère possède des usines à Rouen, lui a-t-il conseillé la ville. Il y peindra lui-même beaucoup. La présence d’une bourgeoisie florissante, potentielle clientèle, attire alors les artistes dans la capitale normande : à la suite de Pissarro, Gauguin y séjournera à son tour. </br> À Rouen, Pissarro, toujours à court d’argent, trouve des conditions d’hébergement facilitées. Le pâtissier parisien Eugène Mürer, homme d’affaires et collectionneur d’art moderne, possède un hôtel place de la République et propose au peintre une location à prix d’ami.</p>

                <p>Pissarro arrive à Rouen au début du mois d’octobre. Il y restera jusqu’au 28 novembre et peindra une quinzaine de tableaux, dont près de la moitié seront achetés par son marchand, Durand Ruel. </p>

                <p>Ce premier séjour, comme les suivants, sont documentés par les nombreuses lettres que Pissarro adresse à ses proches, et notamment à Lucien, son fils aîné. </p>

                <p>Dès le 11 octobre, le peintre est au travail. Il a installé son chevalet en plein air, en retrait de la foule grouillante des quais. Dans une lettre (perdue), Pissarro raconte à Gauguin l’attroupement qui s’était formé quand il avait voulu s’installer dans la rue pour peindre. C’est donc sur la berge en aval du pont Corneille, en contrebas du Pré aux Loups et de l’église Saint-Paul qu’il se place : l’environnement, plus calme, est propice à son activité. </p>

                <p>À cet emplacement, il réalise six tableaux, la plupart regardant vers la ville, à l’exception d’un, qui figure la colline Sainte-Catherine. </p>

                <p>Durant ce même séjour, il s'aventure sur la rive gauche, peu prisée des artistes : il y peint la place Lafayette et le Cours-La-Reine.</p>

                <p>Mais le temps ne lui est pas toujours favorable : à Rouen, la météo est changeante et Pissarro « <i>bûche beaucoup, du moins autant que le temps [lui] permet </i> ». Devant composer avec les caprices du ciel, Pissarro mène de front ses différents tableaux et se déplace selon les heures du jour. Le 19 octobre, il a déjà « <i>neuf toiles en train</i> » </p>

                <p>« <i> Le temps ici est on ne peut plus variable, c’est décourageant ; j’ai neuf toiles en train, plus ou moins avancées. </i> » </p>

                <p>Pissarro aime également se promener dans la ville médiévale. Il y réalise un certain nombre de croquis qui lui inspireront, dans les années qui suivent, un ensemble d'eaux-fortes, gravées de mémoire. </p>

                <p>Les tableaux de 1883 se distinguent de ceux réalisés durant les séjours de 1896 et 1898 par leur caractère plus champêtre. Pissarro se montre encore timide face au sujet urbain. Treize ans plus tard, c'est bien l'agitation du port industriel qui l'attirera dans la capitale normande, tandis que sa maladie des yeux lui interdira de peindre en plein air : tous ses tableaux ou presque seront réalisés depuis la fenêtre de sa chambre d'hôtel. </p>
