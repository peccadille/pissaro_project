<p>Les après-midi ensoleillées l’encouragent à renouer avec le plein air. Il retourne au cours de la Reine où il avait déjà posé son chevalet quinze ans plus tôt. Quelque temps auparavant, il avait confié à son fils « <i>vouloir refaire bien des choses qu’[il] avai[t] étudié[es] dans le temps.</i> »  </p>
<p>Cet ultime séjour est aussi l’occasion d’élire un nouveau motif : la rue de l’Épicerie où se tient tous les vendredis un pittoresque marché. Avec sa foule grouillante, ses maisons biscornues, ses publicités multicolores et sa belle perspective sur la cathédrale, cette petite rue du centre-ville, aujourd’hui défigurée, attire de nombreux peintres, qui, comme Pissarro, adoptent le point de vue surplombant qu’offre la Fierte Saint-Romain. </p>

<p>Des séjours de Pissarro à Rouen, celui de 1898 est le plus long mais aussi le moins documenté : absorbé par le travail, le peintre écrit peu à ses proches, nous privant d’une source essentielle. </p>

