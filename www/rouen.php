              <div class="text_long">
                <p>Quand Pissarro séjourne à Rouen, en 1883, 1896 puis 1898, la capitale Normande est une des plus grandes villes industrielles de France. Cela se lit dans son paysage urbain, où les cheminées d’usines sont omniprésentes, dans l’activité continue de son port et dans l’essor de sa bourgeoisie, enrichie par le commerce et les fabriques. </p>

<p>Rouen présente donc un paysage résolument moderne, et c’est ce que s’attachera principalement à représenter le peintre. </p>

<p>Le port de Rouen s’est profondément modifié au cours des décennies précédentes : de nouveaux bassins ont été creusés, les quais réaménagés, des grues accélèrent désormais le déchargement des bateaux.
Mais faute de place, la plupart des manœuvres sont encore effectuées en pleine ville : ainsi, au pied de l’hôtel d’Angleterre, les débardeurs déchargent le bois et les tonneaux de vin. </p>

<p>Rouen marque une rupture de charge sur la Seine : les marchandises transportées sur de grands voiliers ou des vapeurs ne peuvent poursuivre leur route au-delà des ponts qui franchissent le fleuve à ce niveau. Elles sont donc transférées sur des péniches ou dans des trains, afin de continuer leur route à travers la France. </p>

<p>Sur la rive gauche de la Seine, le paysage est marqué par l’essor de cette intermodalité. Autour du faubourg Saint-Sever, relié au centre-ville par les ponts Boiledieu et Corneille, sont établies deux gares, aujourd’hui disparues : la gare de l’Ouest et la gare d’Orléans, que Pissarro figure de nombreuses fois. Au-delà, vers l’ouest, se dresse la silhouette massive des docks.
À l’opposé, l’île Lacroix conserve des allures champêtres, malgré les usines qui s’y sont implantées. Les bateliers y sont nombreux à y s’être installés. L’île est aussi un lieu de détente dominicale, notamment autour des Folies Bergères et des guinguettes qui parsèment la rive. </p>

<p>Depuis la fenêtre des chambres qu’il loue à l’hôtel de Paris puis à l’hôtel d’Angleterre, Pissarro embrasse toute la boucle de la Seine. Dans ses tableaux, il transcrit tout autant le monumental paysage industriel que l’agitation au pied de sa fenêtre : embarquement et débarquement des passagers du vapeur, dockers au travail… </p>

<p>« <i>J’ai tâché de donner une idée du mouvement, de la vie, de l’atmosphère du port si peuplé de bateaux fumants, des ponts, des cheminées, des quartiers de la ville dans la brume, le brouillard, le soleil couchant, etc. […] Enfin j’ai fait ce que j’ai vu et senti… Il est possible que je me fiche dedans, car ces motifs ne posent pas longtemps, un, deux, trois jours </i> » (lettre 1340)</p>

